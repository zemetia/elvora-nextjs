export function getAllFAQs(): FAQCategory[] {
  return faqData;
}

export function getFAQByCategory(category: string): FAQCategory | undefined {
  return faqData.find(faq => faq.category === category);
}

export function searchFAQs(query: string): FAQCategory[] {
  const lowerQuery = query.toLowerCase();

  return faqData.map(category => ({
    ...category,
    items: category.items.filter(
      item =>
        item.question.toLowerCase().includes(lowerQuery) ||
        item.answer.toLowerCase().includes(lowerQuery)
    )
  })).filter(category => category.items.length > 0);
}
